DATA SEGMENT
   buffersize EQU 256
    inputstring DB buffersize dup(?)
    vowelscount DW 0
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
MOV AX,DATA
MOV DS,AX
LEA DX,inputstring
MOV AH,0AH
INT 21H
MOV CX,0
MOV DI,offset inputstring+1

COUNT_VOWELS:
MOV AL,[DI]
CMP AL,00H
JE END_COUNT
CMP AL,'A'
JE IS_VOWEL
CMP AL,'E'
JE IS_VOWEL
CMP AL,'I'
JE IS_VOWEL
CMP AL,'O'
JE IS_VOWEL
CMP AL,'U'
JE IS_VOWEL
CMP AL,'a'
JE IS_VOWEL
CMP AL,'e'
JE IS_VOWEL
CMP AL,'i'
JE IS_VOWEL
CMP AL,'o'
JE IS_VOWEL
CMP AL,'u'
JE IS_VOWEL
JMP NOT_VOWEL
IS_VOWEL:
INC vowelscount;
JMP CONTINUE
NOT_VOWEL:
CONTINUE:
INC DI
INC CX
CMP CX,buffersize
JGE END_COUNT
JMP COUNT_VOWELS
END_COUNT:
INT 20H;
CODE ENDS
END

